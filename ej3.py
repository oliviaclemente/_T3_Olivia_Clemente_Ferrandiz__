nombre= "Olivia" #nombrar variable (cadena), por lo tanto se pone ""
print(nombre.lower()) #lower() convierte todos los caracteres alfabéticos en minúscula.
print(nombre.upper()) #upper() convierte todos los caracteres alfabéticos en mayúsculas.

